public class SwitchOp {
    public static void main(String[] args) {
        int n = 4;

        switch(n) {
            case 1 :
              System.out.println("one ");
              break;

            case 2 :
              System.out.println("Two");
              break;


            case 3 :
              System.out.println("Three");
              break;
            
            case 4 :
              System.out.println("Four ");
              break;
            default :
              System.out.println("No Match");
        }
    }
    
}
