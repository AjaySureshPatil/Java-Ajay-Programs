import java.util.*;
public class NumberPattern {
    public static void main(String [] args ){
        int n;
        Scanner sc=new Scanner(System.in);
        n=sc.nextInt();
        for(int i=1;i<=n;i++){
            for(int j=1;j<=n;j++){
                System.out.print("4");
            }
            System.out.println();

           
        }
    }
    
}
